# Type casting
# Type casting refers to explicitly changing the data type of an object.
str = "10"
int = int(str)  # Converts string to integer
print(int)  # Output: 10

# Type conversion
# Type conversion refers to implicitly changing the data type of an object during operations.
float = 3.5
result = float + int  # Converts num_int to float before addition
print(result)  # Output: 13.5
